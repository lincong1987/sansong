                        <div class="modal-industry-news-view-content">
                            <p>2012年在线旅游业的价格血战，更让生态圈中所有企业无一幸免。2013年似黑夜后的黎明，10个趋势已历历在目。</p>
                            <p>【环球旅讯】（评论员 鲸涛）2012年对旅游从业者并不是个太好的年份。宏观环境方面，欧债危机加深，国内产业腾笼换鸟，持续多年的超高速GDP成长放缓，压抑了国人的消费能力；而旅游产业的价格血战，更让生态圈中所有企业无一幸免。但展望2013，对特定产业正似黑夜后的黎明，10个趋势已历历在目。</p>
                            <p><strong>趋势一：旅游电商价格战将实质止战</strong></p>
                            <p>携程与艺龙的价格战众所瞩目，虽然双方都口称不会停止，但是身体比嘴巴更诚实，第四季度双方力度都下降许多。价格战在财务上虽是双输格局，但是携程对所有同业传达出了“红线”概念：当竞争者运用削价竞争，导致份额对携程有“影响力”时，携程会不惜割肉应战，“影响力”的定义很可能在携程现有份额的1/3左右。2013年价格竞争永远不会终止，但是力度与范围将会收敛在红线以内。</p>
                            <p><strong>趋势二：美国OTA集团大中华互搏趋势成形</strong></p>
                            <p>大中华区已成美国OTA旅游集团的兵家必争之地。携程与Priceline旗下Booking的合作，激化了艺龙与Expedia的危机感，造成Expedia加大对艺龙的资金流入 ，但这可能更强化携程与Priceline两大旅游集团间的合作关系。2013年，有可能出现成“Priceline + 携程”与“Expedia + 艺龙”在大中华区跨国互搏的局势。</p>
                            <p><strong>趋势三：出境游将比你想得更火爆</strong></p>
                            <p>国内基础工业以及制造业经济的衰退，让人对假日办黄金周旅游收入同比增长44.4%的数据将信将疑。事实上，2013年旅游业大环境将比多数调研预期的更好，而出境游又将比国内游更为畅旺，最大的起因在于经济回温带动的消费动能；其次是消费者消费信心的恢复；当然，还有少部分起因于国内远程游高成本导致的需求转向。</p>
                            <p><strong>趋势四：OTA的商业模式将渗入预付模式</strong></p>
                            <p>OTA的超额利润时代终将结束，但奇怪的是，受到重大打击的反而会是中小代理。受到价格战洗礼，OTA理解了高毛利时代将远去的事实，为了保持竞争力，将会从以中介收佣金的单纯代理模式（Agency Model），逐渐渗入承担库存风险以降低终端售价的预付模式（Merchant Model）。最后，OTA虽然低利但是凭借大量的客户群活得更好，中小代理反而会受冲击。预付模式的渗入速度有多快，取决于OTA对市场需求的预估能有多精准。</p>
                            <p><strong>趋势五：旅游批发商的成长将被限制</strong></p>
                            <p>地区型的批发商（Wholesaler），总是能在去哪儿与淘宝提供比OTA更好的价格。Wholesaler除了将面对OTA预付模式的价格冲击，随着机+酒供应商更重视直销与价格体系，批发商将会逐渐被要求更高的标准，以至于终端售价将与OTA接近，从而降低了成长竞争力。</p>
                            <p><strong>趋势六：开放平台风潮最终对先行旅游企业有利</strong></p>
                            <p>OTA、旅游搜索、旅游平台将因开放平台风潮而殊途同归。淘宝、360、百度本来就是平台，去哪儿与酷讯算半个平台，而现在OTA也走向平台化。没有品牌不希望作出区别化，所以现在OTA放在淘宝、360旅游平台的产品，都是引流量的低毛利棋子；而在OTA彻底平台化后，会在OTA上架的供应商产品也有此发展趋势。</p>
                            <p>更关键的是，随着时间推进和利益驱动，也许2013的某一天，如果隐去平台名称，或许没人能辨识平台服务商是谁。这将会对于先行成熟品牌可能更有利，因为它们更有时间的积累，会更加强调服务经验。</p>
                            <p><strong>趋势七：预订系APP与OTA的数据对接将被证明是饮鸩止渴</strong></p>
                            <p>携程、艺龙、同程都愿意积极开放数据接口给预订系APP，看来是令人振奋的公平公正之举。但当每个APP都能对接到机票/酒店/旅游数据接口时，也就代表了每个APP更同质化，在竞争上又回到了起跑点。</p>
                            <p>因为新创业的预订系APP很少能具备单独与供应商签约的成本与能力，所以必须吃下这个糖衣毒药。要活过这个阶段的APP必须兼有创意与资金，今夜酒店特价是否可以越过募资与损益两平的门槛，值得关注。</p>
                            <p><strong>趋势八：专攻于拓展“旅行中”业务的APP，将更能得到PE与VC的认可</strong></p>
                            <p>没有人会否认移动互联网的成长速度将远远把PC互联网抛到身后，但能享受到资金果实的只有专注“旅行中”业务类型的APP。调研显示目前愿意付费的APP旅客，消费行为集中发生在“当日”与”当地”，极端的数据甚至显示旅客仅预订距现在位置16公里内的酒店。能紧抓这类消费受众的APP，会更容易融资成功。</p>
                            <p><strong>趋势九：旅游搜索的商业模式仍旧存疑</strong></p>
                            <p>不断推迟上市时间，折腾纳斯达克IPO工作人员两年的旅游搜索Kayak，在上市后仅过一季又卖给了Priceline。虽然Kayak所公开的财报并不像Facebook或Groupon般不堪入目，但是是否达到了投资人的预期仍然不得而知。另外，考虑到它所使用的ITA技术，加上ITA被Google收购的事实，Kayak的未来之路其实并不明朗。</p>
                            <p>这对动作频频，计划在美国上市的去哪儿不是好事，考虑到目前五大会计师事务所与SEC的紧张关系，去哪儿与百度的关联交易会被审慎检视。当然，如果去哪儿的收费能从每点击次（CPC）跨越到每服务次（CPS），也许这会是强化该商业模式可持续性的一种方式。</p>
                            <p><strong>趋势十：经济型酒店集团的低端圈地将转向为中端博弈</strong></p>
                            <p>经济型酒店集团一直有着”你做得对，我就学“的勤学不倦格局。在如家、汉庭、锦江、七天四大体系门店总数超越4500间，开业总房间数出现突破50万间的黄金数字后，经营者们的扩张方向号称将转向到高端市场。其实，他们真正的目的是补足中端酒店市场的空白。过去中国酒店在低、高端两头数量多，中端领域发展薄弱的M字架构，未来将逐渐改变。谁会在中端领域取胜看不出来，但是如家若要拿稳门票，必须先把莫泰的问题彻底搞定。</p>
