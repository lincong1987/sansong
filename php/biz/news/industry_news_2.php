                        <div class="modal-industry-news-view-content">
                            <p>笔者谨以此文浅谈2012在线旅游业的八大趋势，尝试预测2012年哪个领域将成为竞相开发的处女地？谁将成为主角，谁又将是最后的赢家？</p>
                            <p>作为国内的新兴产业，在线旅游业近年来备受瞩目，刚过去的2011年，包括腾讯、百度、阿里巴巴在内的多家互联网巨头以及众多的创业企业，相继进入这一领域淘金，团购、逆向拍卖、last-minute、酒店私卖等多种新兴模式和产品不断涌现，成为了这个行业的“搅局者”。</p>
                            <p><strong>趋势一： 在线预订比例加大，呼叫中心成为配角</strong></p>
                            <p>过去十年,中国OTA像一个怪胎，披着在线皮，却干着线下活。随着呼叫中心成本不断升高和电子商务普及，在线策略被越来越多OTA视为运营核心策略。目前，艺龙、芒果网在线比例都超过55%，携程比例大约45%，携程Q4推出优惠券鼓励在线预订,借此来提升在线预订比例。在线预订相比于电话预订，平均每个订单大约节省9块钱，在佣金率逐年走低的情况下，开源节流比增长对利润率贡献更大。</p>
                            <p><strong>趋势二：老树开新花　2011年，创新不再是创业公司专利</strong></p>
                            <p>携程、艺龙、去哪儿都不断对产品和商业模式进行优化，采用最新技术抢占更大市场份额。携程的驴评、酒店团购、高铁、惠选频道在不断地扑捉新的市场机会。艺龙更是成为创新先锋，酒店团购在线预约、last-minute、酒店杀价、微博@订酒店等成功应用，使艺龙成为2011年中国OTA最引人注目公司。去哪儿除了市场公关宣传方面继续对携程穷追猛打以外，拿到巨额融资后加大了对酒店和无线市场投入，推出“酒店越狱”、“夜销”等创新产品。</p>
                            <p><strong>趋势三：市场竞争更激烈，细分领域成为创业方向</strong></p>
                            <p>如果说携程、艺龙、芒果、同程代表OTA 1.0模式；去哪儿、酷讯、淘宝则代表OTA 2.0模式；青芒果、途家、爱日租则代表OTA 3.0模式。1.0模式都以机+酒为核心，强调一站式服务。2.0模式去哪儿、淘宝则代表平台媒体模式，强调流量分享。3.0则代表细分市场精耕细作，青芒果专注经济类酒店在线预订市场、爱日租则专注日租房领域。3.0模式将成为很多新型公司创业方向。</p>
                            <p><strong>趋势四：尾货市场将成为蓝海</strong></p>
                            <p>随着旅游业主对收益管理越来越重视和无线互联网快速发展，Priceline市值不断走高让国内OTA看到这一领域的巨大商机。无论是以PriceLine为模型“我开价”、“越狱”、“惠选”、“酒店杀价”还是Last-minute中国版“今夜酒店特价”等都瞄准这一块市场。而且，尾货市场也在不断细分，包括钟点房、高端度假酒店、邮轮、私家车、闲置物业都成为尾货宠儿。</p>　
                            <p><strong>趋势五：无线互联网三分天下占其一</strong></p>
                            <p>2012年，网络预订、电话预订、无线预订将会并驾齐驱。作为典型O2O模式，无线在OTA领域应用速度明显慢于其它领域，相比58同城、大众点评、口碑网等手机与电脑访问终端流量构成和成交比例,OTA无线应用还有较大成长空间。按照目前智能手机出货量，智能手机普及率将快速增长,网民上网习惯将被改变，真正拇指时代将开启。手机无线预订从现在战略布局变成主要预订方式。</p>
                            <p><strong>趋势六：口水仗将继续</strong></p>
                            <p>2011年，除了腾讯与360之间"3Q大战"以外，携程与去哪儿“去携大战”炒得沸沸扬扬。新年伊始，携程与去哪儿火车票公关战又开始拉开序幕。相信随着不断有新商业模式和竞争者进入这个市场，资源抢夺和阻击战不可避免发生。而且随着艺龙、去哪儿、淘宝、携程实力的相益接近和业务不断重合交叉，利益冲突不断加剧。</p>
                            <p><strong>趋势七：旅游产品组合打包,P2P模式萌芽</strong></p>
                            <p>团购、Last-minute、模糊定价等模式都很好解决价格体系保护问题，同时也降低OTA收入，它既能解决价格保护、又能解决收入问题，产品组合打包明显比其它模式更有优势。而且随着一站式旅游大超市和目的地旅游不断兴起，消费者不但要解决普通住行问题，还有其它如票券、租车、购物、餐饮其它附加增值服务。P2P模式将开始萌芽。</p>
                            <p><strong>趋势八：台湾市场由冷变热</strong></p>
                            <p>2011年，北京、上海、厦门成为首批赴台个人自由行城市，但整体没有达到预期火爆效果。随着马英九连任，两岸关系不断走向缓和，商务合作和旅游市场开放力度将加大。开放赴台自由行城市范围将从目前3个增加至10个，而且年内可能还会继续增加。相信，台湾将继香港、港门成为内地旅客境外游首选之一。</p>
                            <p>最后，笔者仅用四组关键词展望在线旅游的2012——“创新、复杂、热闹、无线”。</p>
                            <p>创新：互联网公司不创新毋宁死，只有创新才能保持时刻领先，只有创新，才是最好卡位战。</p>
                            <p>复杂：国际国内经济形势较为复杂，在线旅游受宏观经济环境影响较大，走势比较复杂。</p>
                            <p>热闹：传统巨头、创业型公司、传统线下资源都纷纷涉足在线旅游，百花齐放。</p>
                            <p>无线：无线互联网快速发展，将成为主要预订方式之一。</p>

