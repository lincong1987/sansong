                            <p>如今在旅游业相关的会议上，都少不了关于社会媒体营销最佳实践的深度探讨。</p>
                            <p>所以我们是时候回到那些基本应用了。以下是十项简单且相对容易实施的技巧，这些技巧是酒店经营者每天可以做到并有望通过它们获得快速回报。且看酒店经营者如何利用它们吧……</p>
                            <p>我们将这些技巧同酒店的日常网络营销经验结合起来。　</p>
                            <p><strong>1、开通博客</strong></p>
                            <p>这很简单。酒店经营者通过博客来发布当地活动、景点、餐厅、购物、夜生活等信息。</p>
                            <p>这让酒店可以为客人提供很多额外的信息。此外，你也可以将这些源源不断的内容延伸到Facebook 和 Twitter上。</p>
                            <p><strong>2、主动出击</strong></p>
                            <p>管理好你的在线声誉，邀请客人在TripAdvisor、Yelp等其他评论网站发表评论。确保你对所有评论及时作出反映，以及在这些网站上及时更新酒店的资料（描述、照片和联系方式等）。</p>
                            <p><strong>3、邀请客人参与</strong></p>
                            <p>邀请客人加入你的Facebook页面（通过电子邮件），并邀请他们发布酒店及旅途的照片。Panoramia就是一个很好的让客人发布照片的网站，因为它与Google Earth和Google Maps链接。</p>
                            <p><strong>4、与当地公司和景点建立链接</strong></p>
                            <p>在你Twitter 或 FaceBook帖子里链接当地公司和景点（通过使用“@+公司名称”的格式）。这能提高你的在线互动，以增强影响力。</p>
                            <p><strong>5、奖励机制</strong></p>
                            <p>适当的奖励是完成工作的推动力。激励措施使人们跃跃欲试。例如，给获得当月最佳照片的客人提供免费的周末住宿，等等……</p>
                            <p><strong>6、鼓励网上签到</strong></p>
                            <p>基于地理位置的签到网站如FourSquare 或Gowalla能够帮你增加曝光度，并促进你的餐饮销售。</p>
                            <p>给通过网上签到来酒店入住的客人提供一杯免费的鸡尾酒，或者是一晚免费的客房升级（根据实际情况），或免费的延迟退房等。</p>
                            <p><strong>7、加入有趣的视频</strong></p>
                            <p>发现目的地有趣的视频，并把它们加入你的YouTube Channel来创建一种视频导游。赞美“视频创造者”，并告诉他们你把视频放在你的官方网站上。</p>
                            <p><strong>8、回收利用</strong></p>
                            <p>重新利用你社交网络的相关内容。关于酒店的积极评论应该贴在Twitter 、Facebook以及博客文章里。从Picasa、Flickr 和Panoramia收集所有客人的照片，把它们放在Facebook的同一个相册里。</p>
                            <p><strong>9、年轻的团队</strong></p>
                            <p>招募一个精通互联网的年轻人来负责社会媒体工作。他们通常能够快速浏览并学习别人好的做法。他（或她）跟年轻客人交谈时比较个性化，这一点很重要。</p>
                            <p><strong>10、展示你幽默的一面</strong></p>
                            <p>有趣且富有个性。 比起正式传统的做法，幽默的做派更胜一筹。你在Facebook上最后真正点击或阅读的文章是什么？我敢肯定是有趣或原创的文章。</p>
                            <p>结论</p>
                            <p>这些简单的步骤将帮助你发展客户群，并与客人进行互动。当然互动不是指简单地通过在线完成，这些技巧和最佳实践还有助于酒店社会媒体营销策略的实施。（Melody 编译）</p>
